/*******************************************************************************
* グローバル変数
*******************************************************************************/
int SleepTime0 = 25;//ゲーム全体の速さのコントロール
int SleepTime1 = 0;//ちらつく場合個々の値を上げる
/*******************************************************************************
 関数名:	int main( void )
 引数　:	void
 戻り値:	正常終了: int型の 0
 説明　:	メイン関数
*******************************************************************************/
int main( void )
{
	Init();			// ゲーム全体の初期化処理
	Sleep(200);     //開始時のちらつき防止（完璧ではない）
	// ゲームモードが終了じゃない間、永久に繰り返す
	while (GetMode() < GAME_END)
	{
		Update();	// ゲーム全体の更新処理

		Draw();		// ゲーム全体の描画処理
		Sleep(SleepTime0+SleepTime1);  //ちらつきの防止用
		
		
		
		
	}

	Uninit();		// ゲーム全体の終了処理

	return 0;
}

void Init(void) {
	title_Init(); 
	draw_tile_Init();
	player_Init();
	bullet_Init();
	enemy_Init();
	
}
void Uninit(void) {
	title_Uninit();
	draw_tile_Uninit();
	player_Uninit();
	bullet_Uninit();
	enemy_Uninit();
	result_Uninit();
}
void Update(void) {
	switch (GetMode()) {
	case GAME_TITLE:
		title_Update();
		break;

	case GAME_MODE:
		enemy_Update();
		bullet_Update();
		player_Update();
		draw_tile_Update();
		GM_Update();
		break;

	case GAME_RESULT:
		result_Update();
		draw_tile_Update();
		break;
	}
}
void Draw(void) {
	switch (GetMode()) {
	case GAME_TITLE:
		title_Draw();
		break;

	case GAME_MODE:
		player_Draw();
		bullet_Draw();
		enemy_Draw();
		draw_tile_Draw();
		break;
	
	case GAME_RESULT:
		result_Draw();
		draw_tile_Draw();
		break;
	}
}


